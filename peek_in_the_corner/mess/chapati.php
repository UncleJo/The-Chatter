                                <p>Curiosity is a very dangerous thing, it can lead you to worst or even the best roads of your life, and curiosity about the taste of food…..well your eyes and tongue have seen a lot many hurdles. One such hurdle is mess chapati.</p>
                                
  <p>When you eat the first bite of the mess chapati, you can easily comprehend that mess chapati wants to talk a lot about the ‘mess’ that has been bestowed in it. This one bite is enough to make you understand that all these years you have underestimated and undegraded home chapati because of all the worldly pleasures that one bite of extra flavours gives you in exchange of few bucks. Every time you chew that one bite of the mess chapati, you will realise that each chewing movement has a different story to convey, first chewing moment will give you the elongated mixed experience of a tasteless chewing gum and chapati struggling to dominate each other.</p>
                                
 <p>As you move on and try to find some taste in that single bite, you will realise that it is not necessary that only the draught or famine struck areas lack grains to make food to fill stomach, thus proving that mess chapati has more hidden qualities in it then one can ever imagine. With the chewing movements increasing , more stories pop out, the mystery of the white powder over the entire chapati grabs attention and not only this, the mixed sensation of hot and cold parts, wet and dry parts, crunchy and soft parts, is enough to arouse your inner feelings for the chef. And as more feelings grow your desire to eat more, gets lost somewhere and eventually that first bite turns out to be the last bite for some of us. In a way a bite of the mess chapati is enough to make us all understand that the stories which just got unfolded were revealed from its scratch. Hence, happy eating.</p>
                                
                                
                                
